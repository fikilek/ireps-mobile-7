export const updateFormState = async (formik, setFormState) => {
  // console.log(`updateFormState formik.values`, formik.values);
  // console.log(`updateFormState called`);
  const { trnState } = formik?.values?.metadata;
  // console.log(`updateFormState trnState`, trnState);

  const { meterAccess } = await formik?.values?.access;
  // console.log(`updateFormState meterAccess`, meterAccess);

  const { dirty, isValid } = formik;
  // console.log(`updateFormState dirty`, dirty);
  // console.log(`updateFormState isValid`, isValid);

  const newState =
    isValid && dirty && meterAccess === "yes" ? "valid" : trnState;
  // console.log(`updateFormState newState`, newState);

  // formik.setFieldValue("metadata.trnState", newState);

  setFormState((prev) => (prev = newState));

  return { state: newState };
};

